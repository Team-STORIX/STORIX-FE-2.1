function normalizeGenreKey(genre: string) {
  const trimmed = genre.trim()
  const enumLike = trimmed.toUpperCase().replace(/[\s-]+/g, '_')

  if (GENRE_SVG[enumLike]) return enumLike
  return genreAliases[trimmed] ?? trimmed
}

function GenreIconItem({ genre, score }: { genre: string; score: number }) {
  const genreKey = normalizeGenreKey(genre)
  const svg = GENRE_SVG[genreKey]
  return (
    <View style={styles.genreItem}>
      {svg ? <SvgXml xml={svg} width={62} height={62} /> : <View style={styles.genreIcon} />}
      <Text style={styles.genreName}>{genreLabels[genreKey] ?? genre}</Text>
      <Text style={styles.genreScore}>{score}점</Text>
    </View>
  )
}

function GenreBarItem({ genre, score }: { genre: string; score: number }) {
  const genreKey = normalizeGenreKey(genre)
  const svg = GENRE_SVG[genreKey]
  return (
    <View style={styles.barContainer}>
      <View style={styles.barBg} />
      <View style={[styles.barPink, { width: `${score}%` as any }]} />
      <View style={styles.barContent}>
        {svg ? <SvgXml xml={svg} width={20} height={20} /> : <View style={styles.barIcon} />}
        <Text style={styles.barName}>{genreLabels[genreKey] ?? genre}</Text>
        <Text style={styles.barScore}>{score}점</Text>
      </View>
    </View>
  )
}

export function ProfilePreferGenreSection() {
  const router = useRouter()
  const { data: genres = [] } = useProfileGenreStats()

  const renderIconSection = () => {
    const top = genres.slice(0, 3)

    if (top.length === 1) {
      return (
        <View style={styles.singleGenre}>
          <GenreIconItem genre={top[0].genre} score={top[0].score} />
        </View>
      )
    }

    if (top.length === 2) {
      return (
        <View style={styles.twoGenres}>
          {top.map((item) => (
            <GenreIconItem key={item.genre} genre={item.genre} score={item.score} />
          ))}
        </View>
      )
    }

    return (
      <View style={styles.threeGenres}>
        {top.map((item) => (
          <GenreIconItem key={item.genre} genre={item.genre} score={item.score} />
        ))}
      </View>
    )
  }

  const renderContent = () => {
    if (genres.length === 0) {
      return (
        <View style={styles.emptyState}>
          <Text style={styles.emptyText}>아직 선호 장르가 없어요...</Text>
          <Pressable
            onPress={() => router.push('/search')}
            style={({ pressed }) => [pressed && styles.pressed]}
            accessibilityRole="button"
            accessibilityLabel="검색으로 이동"
          >
            <Image source={findGenreButton} style={styles.findButton} contentFit="contain" />
          </Pressable>
        </View>
      )
    }

    const extra = genres.slice(3)

    return (
      <>
        {renderIconSection()}
        {extra.length > 0 && (
          <View style={styles.barSection}>
            {extra.map((item) => (
              <GenreBarItem key={item.genre} genre={item.genre} score={item.score} />
            ))}
          </View>
        )}
      </>
    )
  }

  return (
    <View style={styles.container}>
      <Text style={styles.title}>선호 장르</Text>
      <View style={styles.content}>
        {renderContent()}
      </View>
    </View>
  )
}

